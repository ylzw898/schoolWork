            <h2 class="blog-post-title mb-1">2023年住房租赁行业政策盘点与分析</h2>
            <p class="blog-post-meta">2023年12月27日 13:29</p>

            <p>2023年，我国房地产市场仍处下行阶段，呈低迷态势。7月中央政治局会议指出：我国房地产市场供求关系发生重大变化，防范风险是今年房地产工作的主基调，各级政府实施降首付、降利率、认房不认贷等宽松政策，力促房地产行业回到平稳发展轨道，避免风险进一步外溢，但政策效果尚未显现。</p>
            <p>7月中央政治局会议同时还指出：要加大保障性住房建设和供给，积极推动城中村改造和“平急两用”公共基础设施建设，盘活改造各类闲置房产。今年，“三大工程”被多次提及，“三大工程”是住房多元供给改革中的重要内容，也是推动构建房地产发展新模式的重要抓手。</p>
            <p>纵观2023年，在住房租赁市场方面，据不完全统计，中央和地方政府出台160余条政策支持住房租赁行业高质量发展。涉及政策培育、金融支持、税收优惠、市场监管、房源筹集、奖补措施、租金减免等供需两端的方方面面，在政策支持下租赁住房项目商业模式逐渐清晰，市场规模有望进一步增长。</p>
