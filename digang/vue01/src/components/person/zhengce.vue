            <h2 class="blog-post-title mb-1">选择银行的注意事项:</h2><br>
            <dl>
              <dt>1、房贷利率</dt><br>
              <dd>既然银行都是一样的安全靠谱，当然是优先选借款成本最低的，毕竟买房贷款可不是小数目，一点细微的差距可以为你节约几万块钱。所以去银行咨询的时候，首先要了解房贷利率，利率的高低直接关系房贷利息支出多少。</dd>
              <dd>今年央行出台了新的政策，商业银行取消自营性个人住房贷款优惠利率，回复到同期贷款利率水平，实行下限管理，针对不同区域结构和客户结构，对住房贷款实行了差别利率。建议大家多跑几个银行，对比一下就知道了，还要结合其他的优惠。</dd>
              <br><dt>2、还款方式</dt><br>
              <dd>虽然房贷支持的还款方式很多，但是不同的银行对不同的客户可能有不一样，也许指定你只能用某种方式还款，大家要选择最适合自己的还款方式，因为不同的还款在利息上会有差别，比如等额本金和等额本息。</dd>
              <br><dt>3、调息方案</dt><br>
              <dd>银行可在合同期内按月、按季或按年进行调整，也可采用固定利率方式，不同的银行规定不同，可以与客户商量，建议提前了解一些相关的内容再与银行沟通，有的允许客户选择，有的则对客户执行同一调息方式，可以选择最适合自己的。</dd>
              <br><dt>4、罚息</dt><br>
              <dd>现在银行已经可以自主决定房贷罚息利率的权利，每个银行的规定不同，一般会在借款合同中载明的借款利率水平上，加收30%至50%，虽然很多人并不会逾期，但是难免会出现忘记还款情况，所以尽量选择罚息低一点的准没错。</dd>
            </dl>
            <hr>
            <br><h2>房租、房贷同时支出可以一并抵扣个税吗？</h2><br>
            <p>问这个问题的朋友不少，一种情况是买的房子还没有交付，在换房贷的同时租了房子，有两笔支出。还有一种情况是夫妻二人一个在家里住，一个因为工作原因在异地租房。这种情况下是房租还是房贷可以抵扣，还是两者都可以？</p>
            <blockquote class="blockquote">
              <p>一个家庭房贷和房租只能二选一，不能同时抵扣</p>
            </blockquote>
            <p>目前的个税附加扣除相关规定，一个家庭房贷和房租只能二选一，不能同时抵扣，无论是夫妻二人还是单方皆如此。举个例子，小李和妻子二人有一套房贷在还，每月2500元，同时小李在外租房，月租金1500元。那么夫妻二人可以选择其中之一来进行抵扣，抵扣的上限一般为1000元每月，选了房贷就不能选房租。这种情况下可以小李或者妻子一方来抵扣所有的个税附加扣除，等于起征个税的收入提前扣除1000元，或者两个人各扣一定比例。</p>
            <p>纳税人在一个纳税周期（一年）不能同时享受房租、房贷同时扣除，但是在下一个纳税年度申报阶段可以做调整。例如你今天房租抵扣了，明年想用房贷来抵扣，次年提前申报即可。记得在个税app上操作并通知公司。</p><br>
